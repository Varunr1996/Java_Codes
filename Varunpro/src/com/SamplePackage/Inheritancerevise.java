public class Inheritancerevise {

	protected void foodchain1() {
		System.out.println("eagle");
		
	}
	protected void foodchain2() {
		System.out.println("rabbit");
		
	}
	public static void main(String[] args) {
		
		Inheritancerevise a = new Inheritancerevise();
		a.foodchain1();
		a.foodchain2();
	} 
	}
