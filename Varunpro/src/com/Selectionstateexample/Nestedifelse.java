package com.Selectionstateexample;

public class Nestedifelse {

	public static void main(String[] args) {
       int a=10;
       if (a==10) {
    	   if (a>=a) {
    		   System.out.println("a is 10");
    	   } else {
    		   System.out.println("a is not 10");
    	   }
       } else {
    	   System.out.println("a is not equal to 10");
       }
}

}
